    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = tablaCitas.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una cita para modificar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Cita citaOriginal = citasMostradas.get(selectedRow);
        Paciente nuevoPaciente = (Paciente) pacienteComboBox.getSelectedItem();
        Medico nuevoMedico = (Medico) medicoComboBox.getSelectedItem();
        Consultorio nuevoConsultorio = (Consultorio) consultorioComboBox.getSelectedItem();
        String nuevaFechaHora = fechaHoraField.getText();
        ModalidadCita nuevaModalidad = (ModalidadCita) modalidadComboBox.getSelectedItem();

        if (citaOriginal.getEstado() == EstadoCita.ATENDIDA || citaOriginal.getEstado() == EstadoCita.CANCELADA) {
            JOptionPane.showMessageDialog(this, "No se puede modificar una cita atendida o cancelada.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (gestionCitas.modificarCita(citaOriginal, nuevaFechaHora, nuevoMedico, nuevoConsultorio)) {
            citaOriginal.setPaciente(nuevoPaciente);
            citaOriginal.setModalidad(nuevaModalidad);
            JOptionPane.showMessageDialog(this, "Cita modificada exitosamente.");
            buscarCitas();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar la cita. Verifique la disponibilidad.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarButtonActionPerformed

    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed
        Paciente paciente = (Paciente) pacienteComboBox.getSelectedItem();
        Medico medico = (Medico) medicoComboBox.getSelectedItem();
        Consultorio consultorio = (Consultorio) consultorioComboBox.getSelectedItem();
        String fechaHora = fechaHoraField.getText();
        ModalidadCita modalidad = (ModalidadCita) modalidadComboBox.getSelectedItem();

        if (paciente == null || medico == null || consultorio == null || fechaHora.isEmpty() || fechaHora.equals("yyyy/mm/dd hh:mm")) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (gestionCitas.crearCita(fechaHora, medico, consultorio, paciente, modalidad)) {
            JOptionPane.showMessageDialog(this, "Cita creada exitosamente.");
            buscarCitas();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo crear la cita. Verifique la disponibilidad.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_crearButtonActionPerformed

