    private ArrayList<Medico> getMedicos() {
        ArrayList<Medico> medicos = new ArrayList<>();
        for (Empleado empleado : gestionEmpleados.getEmpleados()) {
            if (empleado instanceof Medico) {
                medicos.add((Medico) empleado);
            }
        }
        return medicos;
    }

    private ArrayList<String> getEspecialidades() {
        ArrayList<String> especialidades = new ArrayList<>();
        for (Medico med : getMedicos()) {
            if (!especialidades.contains(med.getEspecialidad())) {
                especialidades.add(med.getEspecialidad());
            }
        }
        return especialidades;
    }

    private void buscarCitas() {
        String especialidad = (String) especialidadFilterComboBox.getSelectedItem();
        String fecha = fechaFilterField.getText();

        ArrayList<Cita> resultado;

        if (especialidad != null && !especialidad.equals("Todas")) {
            resultado = gestionCitas.buscarCitaPorEspecialidad(especialidad);
        } else {
            resultado = gestionCitas.getCitas();
        }

        if (fecha != null && !fecha.trim().isEmpty() && !fecha.equals("yyyy/mm/dd")) {
            ArrayList<Cita> citasPorFecha = new ArrayList<>();
            for (Cita c : resultado) {
                if (c.getFechaHora().startsWith(fecha.trim())) {
                    citasPorFecha.add(c);
                }
            }
            resultado = citasPorFecha;
        }

        actualizarTabla(resultado);
    }

    private void cargarDatosDeCitaSeleccionada() {
        int selectedRow = tablaCitas.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }

        Cita citaSeleccionada = citasMostradas.get(selectedRow);
        pacienteComboBox.setSelectedItem(citaSeleccionada.getPaciente());
        medicoComboBox.setSelectedItem(citaSeleccionada.getMedico());
        consultorioComboBox.setSelectedItem(citaSeleccionada.getConsultorio());
        fechaHoraField.setText(citaSeleccionada.getFechaHora());
        modalidadComboBox.setSelectedItem(citaSeleccionada.getModalidad());
    }

    private void actualizarTabla(ArrayList<Cita> citas) {
        tableModel.setRowCount(0);
        this.citasMostradas = citas;
        for (Cita cita : citas) {
            Object[] row = {
                cita.getFechaHora(),
                cita.getPaciente(),
                cita.getMedico(),
                cita.getMedico().getEspecialidad(),
                cita.getConsultorio().getCodigo(),
                cita.getEstado()
            };
            tableModel.addRow(row);
        }
    }

    private void limpiarCampos() {
        pacienteComboBox.setSelectedIndex(-1);
        medicoComboBox.setSelectedIndex(-1);
        consultorioComboBox.setSelectedIndex(-1);
        fechaHoraField.setText("yyyy/mm/dd hh:mm");
        modalidadComboBox.setSelectedIndex(0);
        tablaCitas.clearSelection();
    }

