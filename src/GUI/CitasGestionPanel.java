    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        int selectedRow = tablaCitas.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una cita para cancelar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Cita citaSeleccionada = citasMostradas.get(selectedRow);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea cancelar esta cita?", "Confirmar Cancelación", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if (gestionCitas.cancelarCita(citaSeleccionada)) {
                JOptionPane.showMessageDialog(this, "Cita cancelada.");
                buscarCitas();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo cancelar la cita.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

