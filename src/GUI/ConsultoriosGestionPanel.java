    private void cargarDatosDeFilaSeleccionada() {
        int selectedRow = tablaConsultorios.getSelectedRow();
        codigoField.setText(tableModel.getValueAt(selectedRow, 0).toString());
        especialidadField.setText(tableModel.getValueAt(selectedRow, 1).toString());
        estadoComboBox.setSelectedItem(tableModel.getValueAt(selectedRow, 2));
    }

    private void cargarComboBox() {
        estadoComboBox.setModel(new DefaultComboBoxModel<>(EstadoConsultorio.values()));
    }

    private void actualizarTabla() {
        tableModel.setRowCount(0);
        ArrayList<Consultorio> lista = gestionConsultorios.getConsultorios();
        for (Consultorio consultorio : lista) {
            Object[] row = {
                consultorio.getCodigo(),
                consultorio.getEspecialidad(),
                consultorio.getEstado()
            };
            tableModel.addRow(row);
        }
    }

    private void limpiarCampos() {
        codigoField.setText("");
        especialidadField.setText("");
        estadoComboBox.setSelectedIndex(0);
        tablaConsultorios.clearSelection();
    }

