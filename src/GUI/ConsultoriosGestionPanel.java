    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        int selectedRow = tablaConsultorios.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un consultorio para eliminar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int codigo = (int) tableModel.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar el consultorio " + codigo + "?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if (gestionConsultorios.eliminarConsultorio(codigo)) {
                JOptionPane.showMessageDialog(this, "Consultorio eliminado.");
                actualizarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el consultorio.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        int selectedRow = tablaConsultorios.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un consultorio para modificar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int codigoOriginal = (int) tableModel.getValueAt(selectedRow, 0);
            int codigoNuevo = Integer.parseInt(codigoField.getText());
            String especialidad = especialidadField.getText();
            EstadoConsultorio estado = (EstadoConsultorio) estadoComboBox.getSelectedItem();

            if (gestionConsultorios.modificarConsultorio(codigoOriginal, especialidad, null, null, estado, codigoNuevo)) {
                JOptionPane.showMessageDialog(this, "Consultorio modificado con éxito.");
                actualizarTabla();
            } else {
                JOptionPane.showMessageDialog(this, "Error al modificar. El nuevo código podría ya existir.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El código debe ser un número válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarButtonActionPerformed

    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed
        try {
            int codigo = Integer.parseInt(codigoField.getText());
            String especialidad = especialidadField.getText();
            EstadoConsultorio estado = (EstadoConsultorio) estadoComboBox.getSelectedItem();

            if (especialidad.isEmpty()) {
                JOptionPane.showMessageDialog(this, "La especialidad es obligatoria.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Consultorio nuevoConsultorio = new Consultorio(codigo, especialidad, estado);
            if (gestionConsultorios.crearConsultorio(nuevoConsultorio)) {
                JOptionPane.showMessageDialog(this, "Consultorio creado exitosamente.");
                actualizarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo crear el consultorio. El código ya podría existir.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El código debe ser un número válido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_crearButtonActionPerformed

